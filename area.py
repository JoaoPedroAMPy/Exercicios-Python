#Retângulo

print("Área do retângulo\n")
aR = float (input("Informe a altura: "))

bR = float (input("Informe a base: "))

areaR = bR * aR


print("A área do Retângulo é: ",areaR)
print(" ")

#Triângulo

print("Área do triângulo\n")
aT = float (input("Informe a altura: "))

bT = float (input("Informe a base: "))

areaT = (bT * aT) / 2

print("A área do Triângulo é: ",areaT)
print(" ")

#Círculo

